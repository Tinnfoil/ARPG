	public int[] randDirection(int[][] grid, int i, int j){
		Random RN= new Random();
		int[] coords= new int[2];
		coords[0]=i;
		coords[1]=j;
		boolean valid=false;
		while(valid==false){
		int num=RN.nextInt(4)+1;
			if(num==1&&j+1<grid[0].length&&grid[i][j+1]==1){//RIGHT
				coords[0]=i;
				coords[1]=j+1;
				System.out.println("RIGHT");
				valid=true;
			}
			else if(num==2&&i+1<grid.length&&grid[i+1][j]==1){//DOWN
				coords[0]=i+1;
				coords[1]=j;
				System.out.println("DOWN");
				valid=true;
			}
			else if(num==3&&j>0&&grid[i][j-1]==1){//LEFT
				coords[0]=i;
				coords[1]=j-1;
				System.out.println("LEFT");
				valid=true;
			}
			else if(num==4&&i>0&&grid[i-1][j]==1){//UP
				coords[0]=i-1;
				coords[1]=j;
				System.out.println("UP");
				valid=true;
			}
			else if((i+1<grid.length&&grid[i+1][j]==1)||(j+1<grid[0].length&&grid[i][j+1]==1)||(i>0&&grid[i-1][j]==1)||(j>0&&grid[i][j-1]==1)){
				valid=false;
			}
			else{
				coords[0]=-1;
				coords[1]=-1;
				valid=true;
			}
		}
		return coords;
	}
	
